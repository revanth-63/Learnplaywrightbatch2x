// Identifier naming style examples

// camelcase first word lower case, next words capitalized
var firstName = "Pramod";
var lastName = "Dutta";

// snakecase words separated by underscores
var first_name = "Amit";
var last_name = "Kumar";

// pascalCase: every word starts with a capital letter
var UserName = "Pramod";
var UserAge = 30;

// kebabcase is invalid in JavaScript identifiers
// var first-name = "Invalid";

// uppersnakecase often used for constants
var MAX_COUNT = 100;

console.log(firstName, lastName, first_name, last_name, UserName, UserAge, MAX_COUNT);

